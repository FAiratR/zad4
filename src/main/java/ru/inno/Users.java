package ru.inno;

public class Users {
    int id;
    String username;
    String fio;

    public void setId(int id) {
        this.id = id;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setFio(String fio) {
        this.fio = fio;
    }

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getFio() {
        return fio;
    }
}
